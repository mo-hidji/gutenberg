// close btn
function closeMenu() {
    document.getElementById('sideMenu').style.transform = 'translateX(100%)';
}

// OPEN btn
function openMenu() {
    document.getElementById('sideMenu').style.transform = 'translateX(0%)';
}

// Close notification
function closeNotif() {
    document.getElementById('notif').style.transform = 'translateX(150%)';
}